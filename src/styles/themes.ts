export type ThemeMode = 'light' | 'dark' | 'system'
export type ResolvedThemeMode = 'light' | 'dark'
export type CornerStyle = 'compact' | 'standard' | 'soft'
export type ThemeVariables = Record<string, string>

export interface ThemePreset {
  id: string
  name: string
  description: string
  modes: Record<ResolvedThemeMode, ThemeVariables>
}

interface PaletteInput {
  bg: string
  bgSecondary: string
  bgTertiary: string
  surface: string
  surfaceHover: string
  border: string
  borderLight: string
  borderFocus: string
  textPrimary: string
  textSecondary: string
  textTertiary: string
  textMuted: string
  textInverse: string
  accent: string
  accentHover: string
  accentLight: string
  accentText: string
  accentSoft: string
  accentContrast: string
  selectionBg: string
  selectionText: string
  titlebarBg: string
  titlebarFg: string
  scrollbar: string
  scrollbarHover: string
  shadowColor: string
  shadowStrength: 'light' | 'dark'
}

function getLuminance(color: string): number {
  const channels = [1, 3, 5].map((index) => Number.parseInt(color.slice(index, index + 2), 16) / 255)
  const [r, g, b] = channels.map((value) => (value <= 0.03928 ? value / 12.92 : ((value + 0.055) / 1.055) ** 2.4))
  return r * 0.2126 + g * 0.7152 + b * 0.0722
}

function getContrastRatio(a: string, b: string): number {
  const lighter = Math.max(getLuminance(a), getLuminance(b))
  const darker = Math.min(getLuminance(a), getLuminance(b))
  return (lighter + 0.05) / (darker + 0.05)
}

function ensureAccentContrast(accent: string, preferred: string): string {
  if (getContrastRatio(accent, preferred) >= 4.5) return preferred
  return getContrastRatio(accent, '#ffffff') >= getContrastRatio(accent, '#000000') ? '#ffffff' : '#000000'
}

function createPalette(input: PaletteInput): ThemeVariables {
  const darkShadow = input.shadowStrength === 'dark'
  return {
    '--color-bg': input.bg,
    '--color-bg-secondary': input.bgSecondary,
    '--color-bg-tertiary': input.bgTertiary,
    '--color-surface': input.surface,
    '--color-surface-hover': input.surfaceHover,
    '--color-border': input.border,
    '--color-border-light': input.borderLight,
    '--color-border-focus': input.borderFocus,
    '--color-text-primary': input.textPrimary,
    '--color-text-secondary': input.textSecondary,
    '--color-text-tertiary': input.textTertiary,
    '--color-text-muted': input.textMuted,
    '--color-text-inverse': input.textInverse,
    '--color-accent': input.accent,
    '--color-accent-hover': input.accentHover,
    '--color-accent-light': input.accentLight,
    '--color-accent-text': input.accentText,
    '--color-accent-soft': input.accentSoft,
    '--color-accent-contrast': ensureAccentContrast(input.accent, input.accentContrast),
    '--color-calendar-selection-bg': input.selectionBg,
    '--color-calendar-selection-border': input.accentHover,
    '--color-calendar-selection-text': input.selectionText,
    '--color-calendar-selection-handle': input.accent,
    '--color-titlebar-bg': input.titlebarBg,
    '--color-titlebar-fg': input.titlebarFg,
    '--color-scrollbar': input.scrollbar,
    '--color-scrollbar-hover': input.scrollbarHover,
    '--shadow-card': darkShadow
      ? `0 1px 3px ${input.shadowColor}, 0 1px 2px rgba(0, 0, 0, 0.18)`
      : `0 1px 3px ${input.shadowColor}, 0 1px 2px rgba(15, 23, 42, 0.04)`,
    '--shadow-card-hover': darkShadow
      ? `0 8px 24px ${input.shadowColor}, 0 2px 6px rgba(0, 0, 0, 0.22)`
      : `0 8px 24px ${input.shadowColor}, 0 2px 6px rgba(15, 23, 42, 0.05)`,
    '--shadow-dropdown': darkShadow
      ? `0 16px 40px ${input.shadowColor}, 0 4px 12px rgba(0, 0, 0, 0.3)`
      : `0 16px 40px ${input.shadowColor}, 0 4px 12px rgba(15, 23, 42, 0.06)`,
    '--shadow-modal': darkShadow
      ? `0 28px 64px ${input.shadowColor}, 0 10px 24px rgba(0, 0, 0, 0.34)`
      : `0 28px 64px ${input.shadowColor}, 0 10px 24px rgba(15, 23, 42, 0.08)`,
  }
}

